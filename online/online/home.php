<h1 style="text-align: center;"> Introduction to Online Examination System</h1>
<p style="line-height: 30px;font-size: 16px">&ensp;&ensp;&ensp;&ensp;&ensp;&ensp;&ensp;&ensp;&ensp;&ensp;Over the years, educational system has evolved to great levels and the credit majorly goes to the technological advancements.

With the introduction of Learning Management Systems and smart classrooms, teachers have been given the power to make their teaching ecosystem more interactive and easy to track the overall progress.Similarly, the advent of online examination system has also brought some huge advancement to the conventional assessment system. It has made the assessment method entirely automated, comprehensive, accurate and instant. Such technological evolution has not just brought ease to the teachers/educators, but has also introduced a lot of benefits for the students and test takers.</p>


<div class="exam"><h1 style="text-align: center;"> What is an Online Examination System</h1>
<p style="line-height: 30px;font-size: 16px">&ensp;&ensp;&ensp;&ensp;&ensp;&ensp;&ensp;&ensp;&ensp;&ensp;Online Examination System is a technology-driven way to simplify examination activities like defining exam patterns with question banks, defining exam timer, objective/ subjective question sections, conducting exams using the computer or mobile devices in a paperless manner.

Online Examination System is a cost-effective, scalable way to convert traditional pen and paper-based exams to online and paperless mode.

Candidates can appear for the exam using any desktop, laptop, or mobile device with a browser. Exam results can be generated instantly for the objective type of questions.

It can simplify overall examination management and result in generation activity.</p></div>



