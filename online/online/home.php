<p style="line-height: 30px;font-size: 16px">&ensp;&ensp;&ensp;&ensp;&ensp;&ensp;&ensp;&ensp;&ensp;&ensp;It is used by Coaching, institutes, colleges, universities, government and corporate to conduct online assessments with ease.</p>



<div class="exam1"><h1 style="text-align: center;"> How does Online Examination System works?</h1>
<p style="line-height: 30px;font-size: 16px">&ensp;&ensp;&ensp;&ensp;&ensp;&ensp;&ensp;&ensp;&ensp;&ensp;In Online Examination System the entire steps involved in a paper-based test right from the beginning till the end is digitized with the use of online assessment software. Let’s read how each step is automated with the application of online exam platform-

<br>&ensp;&ensp;&ensp;&ensp;&ensp;&ensp;&ensp;&ensp;&ensp;&ensp;<b>Creation of test-</b>
The administrator has got multiple options to create an online test that is interactive as well as effective. Online test creation platform provides variety of question types using which a subjective, objective or MCQ type test can be easily created. Also the choice of questions can be done from an existing test library or the test creator can create an all new set of questions by uploading them in just a click.

<br>&ensp;&ensp;&ensp;&ensp;&ensp;&ensp;&ensp;&ensp;&ensp;&ensp;<b>Conducting of test-</b>
Conducting a large scale test is not an issue when it comes to online test. Online exam software has got the feature to accommodate even a large pool of students at once. The additional feature of “candidate management” increases the feasibility to conduct a test to a large group of candidates in a systematic manner. It allows the test taker to categorize the candidates in their respective groups and at the time of conducting the test, the test taker can assign individual test to every group, conducting multiple tests at once.</p></div>


